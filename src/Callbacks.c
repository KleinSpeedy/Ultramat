void on_Run_Pos1_toggled(GtkToggleButton *toggle_button)
{

    if (gtk_toggle_button_get_active(toggle_button))
    {
        g_print("Button 1 is ON \n");
    }
    else
    {
        g_print("Button 1 is OFF \n");
    }
}

